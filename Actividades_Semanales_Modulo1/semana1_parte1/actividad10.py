# Potencias con f-strings, Pide un número entero y muestra el número elevado a la 2 y a la 3 usando f-strings.

num1 = int(input("Ingrese el numero a consultar: \n>"))

resultado1 = num1 ** 2
resultado2 = num1 ** 3

print(f"El resultado del numero ingresado al cuadrado es: {resultado1} y al cubo es: {resultado2}")
