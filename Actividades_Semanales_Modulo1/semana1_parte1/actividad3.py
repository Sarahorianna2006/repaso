#Multiplicación de decimales; Pide dos números decimales (float) y muestra su multiplicación.

num1 = float(input("Ingrese el primer numero con decimal (separado por: . ) a consultar:  \n>"))
num2 = float(input("Ingrese el segundo numero con decimal (separado por: . )a consultar: \n>"))

multiplicacion = num1 * num2

print(f"El resultado de la multiplicacion de ambos numeros es: {multiplicacion}")
